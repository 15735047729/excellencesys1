package com.example.excellencesys.pojo;


public class County {

  private long coid;
  private String coname;
  private long cid;


  public long getCoid() {
    return coid;
  }

  public void setCoid(long coid) {
    this.coid = coid;
  }


  public String getConame() {
    return coname;
  }

  public void setConame(String coname) {
    this.coname = coname;
  }


  public long getCid() {
    return cid;
  }

  public void setCid(long cid) {
    this.cid = cid;
  }

}
